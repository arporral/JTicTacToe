    private void marcarJugada(int i, javax.swing.JButton jButton) {
        if (pantallaCompleta) {            
            if (JOptionPane.showConfirmDialog(this, "No hay mÃ¡s jugadas disponibles. Desea volver a empezar?", "Pantalla completa", YES_NO_OPTION) == 0) {
                this.dispose();
                empezarJuego();                
            }            
        } else {
            if (jugadas[i].equals("")) {
                jugadas[i] = jugador;
                jButton.setText(jugadas[i]);
                calcularGanador();
            } else {
                JOptionPane.showMessageDialog(this, "Jugada incorrecta. Casilla ya ocupada.");
            }
        }
    }

    private void calcularGanador() {
        String ganador = "";

        if (jugadas[0].equals(jugadas[1]) && jugadas[0].equals(jugadas[2]) && !jugadas[0].equals("")) {
            ganador = jugadas[0];
        } else if (jugadas[3].equals(jugadas[4]) && jugadas[3].equals(jugadas[5]) && !jugadas[3].equals("")) {
            ganador = jugadas[3];
        } else if (jugadas[6].equals(jugadas[7]) && jugadas[6].equals(jugadas[8]) && !jugadas[6].equals("")) {
            ganador = jugadas[6];
        } else if (jugadas[0].equals(jugadas[3]) && jugadas[0].equals(jugadas[6]) && !jugadas[0].equals("")) {
            ganador = jugadas[0];
        } else if (jugadas[1].equals(jugadas[4]) && jugadas[1].equals(jugadas[7]) && !jugadas[1].equals("")) {
            ganador = jugadas[1];
        } else if (jugadas[2].equals(jugadas[5]) && jugadas[2].equals(jugadas[8]) && !jugadas[2].equals("")) {
            ganador = jugadas[2];
        } else if (jugadas[0].equals(jugadas[4]) && jugadas[0].equals(jugadas[8]) && !jugadas[0].equals("")) {
            ganador = jugadas[0];
        } else if (jugadas[2].equals(jugadas[4]) && jugadas[2].equals(jugadas[6]) && !jugadas[2].equals("")) {
            ganador = jugadas[2];
        }

        if (ganador.equals("X")) {            
            if (JOptionPane.showConfirmDialog(this, "El jugador 1 ha ganado. Desea volver a empezar?", "GanÃ³ jugador 1", YES_NO_OPTION) == 0) {
                this.dispose();
                empezarJuego();
            } else {
                pantallaCompleta = true;
                return;
            }
        } else if (ganador.equals("O")) {
            if (JOptionPane.showConfirmDialog(this, "El jugador 2 ha ganado. Desea volver a empezar?", "GanÃ³ jugador 2", YES_NO_OPTION) == 0) {
                this.dispose();
                empezarJuego();
            } else {
                pantallaCompleta = true;
                return;
            }
        }

        pantallaCompleta=true;
        
        for (int j = 0; j < 9; j++) {
            if (jugadas[j].equals("")) {
                pantallaCompleta = false;
            }
        }
        
        if (pantallaCompleta) {
            JOptionPane.showMessageDialog(this, "Pantalla completa. No hay mÃ¡s jugadas disponibles.");
            return;
        }
        if (jugador.equals("X")) {
            jugador = "O";
            txtpantalla.setText("JUGADOR 2");
        } else {
            jugador = "X";
            txtpantalla.setText("JUGADOR 1");
        }
    }

